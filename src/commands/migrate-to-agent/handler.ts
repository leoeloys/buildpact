/**
 * Migrate-to-agent command handler.
 * Validates existing BuildPact artifacts and generates Agent Mode configuration.
 * @see FR-1051 — Prompt-to-Agent Mode Migration Path
 * @see US-047 — Epic 10.5: Prompt-to-Agent Mode Migration Path (v2.0)
 */

import * as clack from '@clack/prompts'
import { readFile, mkdir, writeFile } from 'node:fs/promises'
import { join } from 'node:path'
import { ok, err, ERROR_CODES } from '../../contracts/errors.js'
import type { Result } from '../../contracts/errors.js'
import type { CommandHandler } from '../registry.js'
import type { SupportedLanguage } from '../../contracts/i18n.js'
import { createI18n } from '../../foundation/i18n.js'
import { AuditLogger } from '../../foundation/audit.js'

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** Status of a file checked during migration validation */
export type FileStatus = 'present' | 'missing'

/** An item in the compatibility report */
export interface CompatibilityIssue {
  /** Relative path of the file (relative to projectDir) */
  file: string
  /** Whether the file is present or missing */
  status: FileStatus
  /** Remediation step if status is 'missing' */
  remediation?: string
}

/** Result of validating .buildpact/ structure */
export interface ValidationResult {
  issues: CompatibilityIssue[]
  /** true when all required files are present */
  allPresent: boolean
}

// ---------------------------------------------------------------------------
// Validation helpers
// ---------------------------------------------------------------------------

/** Required files for Agent Mode migration */
const REQUIRED_FILES: Array<{ rel: string; remediation_en: string; remediation_pt: string }> = [
  {
    rel: '.buildpact/config.yaml',
    remediation_en: 'Run `npx buildpact init` to initialise project configuration.',
    remediation_pt: 'Execute `npx buildpact init` para inicializar a configuração do projeto.',
  },
  {
    rel: '.buildpact/constitution.md',
    remediation_en:
      'Run `npx buildpact init` or create .buildpact/constitution.md manually with your project rules.',
    remediation_pt:
      'Execute `npx buildpact init` ou crie .buildpact/constitution.md manualmente com as regras do projeto.',
  },
  {
    rel: '.buildpact/project-context.md',
    remediation_en:
      'Create .buildpact/project-context.md with domain description, architecture overview, and key conventions.',
    remediation_pt:
      'Crie .buildpact/project-context.md com descrição do domínio, visão geral da arquitetura e convenções principais.',
  },
]

/**
 * Validate existing .buildpact/ files required for Agent Mode.
 * Returns an array of CompatibilityIssues with file-level status and remediation.
 */
export async function validateBuildpactFiles(
  projectDir: string,
  lang: SupportedLanguage = 'en',
): Promise<ValidationResult> {
  const issues: CompatibilityIssue[] = []

  for (const entry of REQUIRED_FILES) {
    const absPath = join(projectDir, entry.rel)
    let present = false
    try {
      await readFile(absPath, 'utf-8')
      present = true
    } catch {
      present = false
    }

    if (present) {
      issues.push({ file: entry.rel, status: 'present' })
    } else {
      issues.push({
        file: entry.rel,
        status: 'missing',
        remediation: lang === 'pt-br' ? entry.remediation_pt : entry.remediation_en,
      })
    }
  }

  const allPresent = issues.every((i) => i.status === 'present')
  return { issues, allPresent }
}

// ---------------------------------------------------------------------------
// Agent Mode config builder
// ---------------------------------------------------------------------------

/**
 * Build the content of .buildpact/agent-mode.yaml.
 * The mode is set to 'prompt' by default — user must change to 'agent' to activate.
 */
export function buildAgentModeConfig(squadName: string | undefined, nowMs: number = Date.now()): string {
  const generatedAt = new Date(nowMs).toISOString()
  const squadLine = squadName ? `  squad: ${squadName}` : '  squad: ~  # set to your squad name'

  return [
    '# BuildPact Agent Mode Configuration',
    `# Generated by: buildpact migrate-to-agent`,
    `# Generated at: ${generatedAt}`,
    '#',
    '# To activate Agent Mode: change mode from "prompt" to "agent"',
    '# The project continues working in Prompt Mode until you make that change.',
    '',
    '# Current operating mode',
    '# Options: prompt | agent',
    'mode: prompt',
    '',
    '# Agent Mode capabilities (enabled when mode is set to agent)',
    'agent_capabilities:',
    '  autonomous_execution: true',
    '  multi_agent_coordination: true',
    '  parallel_wave_execution: true',
    '',
    '# Active squad for agent coordination',
    'squad_config:',
    squadLine,
    '',
  ].join('\n')
}

