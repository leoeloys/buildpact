/** Read language from .buildpact/config.yaml, fallback to 'en' */
async function readLanguage(projectDir: string): Promise<SupportedLanguage> {
  try {
    const content = await readFile(join(projectDir, '.buildpact', 'config.yaml'), 'utf-8')
    for (const line of content.split('\n')) {
      const trimmed = line.trim()
      if (trimmed.startsWith('language:')) {
        const value = trimmed.slice('language:'.length).trim().replace(/^["']|["']$/g, '')
        if (value === 'pt-br' || value === 'en') return value
      }
    }
  } catch {
    // Config missing — fall back to English
  }
  return 'en'
}

/** Read active_squad from .buildpact/config.yaml, returns undefined if not set */
async function readActiveSquadName(projectDir: string): Promise<string | undefined> {
  try {
    const content = await readFile(join(projectDir, '.buildpact', 'config.yaml'), 'utf-8')
    for (const line of content.split('\n')) {
      const trimmed = line.trim()
      if (trimmed.startsWith('active_squad:')) {
        const value = trimmed.slice('active_squad:'.length).trim().replace(/^["']|["']$/g, '')
        if (value && value !== 'none') return value
      }
    }
  } catch {
    // Config missing
  }
  return undefined
}

// ---------------------------------------------------------------------------
// Handler
// ---------------------------------------------------------------------------

export const handler: CommandHandler = {
  async run(_args: string[]): Promise<Result<void>> {
    const projectDir = process.cwd()
    const lang = await readLanguage(projectDir)
    const i18n = createI18n(lang)
    const audit = new AuditLogger(join(projectDir, '.buildpact', 'audit', 'audit.log'))

    clack.intro(i18n.t('cli.migrate_to_agent.welcome'))

    // Log intent
    await audit.log({
      action: 'migrate_to_agent.start',
      agent: 'migrate-to-agent',
      files: [],
      outcome: 'success',
    })

    const spinner = clack.spinner()
    spinner.start(i18n.t('cli.migrate_to_agent.validating'))

    // Step 1: Validate existing files
    const { issues, allPresent } = await validateBuildpactFiles(projectDir, lang)

    spinner.stop(i18n.t('cli.migrate_to_agent.validated'))

    // Report each file status
    for (const issue of issues) {
      if (issue.status === 'present') {
        clack.log.success(`${issue.file}`)
      } else {
        clack.log.warn(`${issue.file} — ${i18n.t('cli.migrate_to_agent.missing')}`)
        if (issue.remediation) {
          clack.log.info(issue.remediation)
        }
      }
    }

    if (!allPresent) {
      clack.log.warn(i18n.t('cli.migrate_to_agent.warn_missing_files'))
    }

    // Step 2: Read squad name for config generation
    const squadName = await readActiveSquadName(projectDir)

    // Step 3: Generate agent-mode.yaml
    const agentModeConfigPath = join(projectDir, '.buildpact', 'agent-mode.yaml')
    const agentModeContent = buildAgentModeConfig(squadName)

    try {
      await mkdir(join(projectDir, '.buildpact'), { recursive: true })
      await writeFile(agentModeConfigPath, agentModeContent, 'utf-8')
    } catch (cause) {
      const result = err<void>({
        code: ERROR_CODES.FILE_WRITE_FAILED,
        i18nKey: 'error.file.write_failed',
        params: { path: agentModeConfigPath, reason: String(cause) },
        cause,
      })
      await audit.log({
        action: 'migrate_to_agent.write_config',
        agent: 'migrate-to-agent',
        files: [],
        outcome: 'failure',
        error: String(cause),
      })
      return result
    }

    clack.log.success(i18n.t('cli.migrate_to_agent.config_written', { path: agentModeConfigPath }))

    // Step 4: Write compatibility report
    const reportPath = join(projectDir, '.buildpact', 'agent-mode-compatibility.md')
    const reportContent = buildCompatibilityReport(issues, lang)

    try {
      await writeFile(reportPath, reportContent, 'utf-8')
    } catch (cause) {
      const result = err<void>({
        code: ERROR_CODES.FILE_WRITE_FAILED,
        i18nKey: 'error.file.write_failed',
        params: { path: reportPath, reason: String(cause) },
        cause,
      })
      await audit.log({
        action: 'migrate_to_agent.write_report',
        agent: 'migrate-to-agent',
        files: [],
        outcome: 'failure',
        error: String(cause),
      })
      return result
    }

    clack.log.success(i18n.t('cli.migrate_to_agent.report_written', { path: reportPath }))

    await audit.log({
      action: 'migrate_to_agent.complete',
      agent: 'migrate-to-agent',
      files: [agentModeConfigPath, reportPath],
      outcome: 'success',
    })

    clack.outro(i18n.t('cli.migrate_to_agent.outro'))

    return ok(undefined)
  },
}
