// Compatibility report builder
// ---------------------------------------------------------------------------

/**
 * Build the markdown compatibility report content.
 * Lists validation results with file references and remediation steps.
 */
export function buildCompatibilityReport(
  issues: CompatibilityIssue[],
  lang: SupportedLanguage,
  nowMs: number = Date.now(),
): string {
  const generatedAt = new Date(nowMs).toISOString()
  const isEn = lang !== 'pt-br'

  const title = isEn
    ? '# BuildPact Agent Mode Compatibility Report'
    : '# Relatório de Compatibilidade — Modo Agente BuildPact'

  const subtitle = isEn
    ? `Generated: ${generatedAt}`
    : `Gerado em: ${generatedAt}`

  const overviewTitle = isEn ? '## Overview' : '## Visão Geral'
  const presentCount = issues.filter((i) => i.status === 'present').length
  const missingCount = issues.filter((i) => i.status === 'missing').length

  const overviewBody = isEn
    ? `- Files checked: ${issues.length}\n- Present: ${presentCount}\n- Missing: ${missingCount}`
    : `- Arquivos verificados: ${issues.length}\n- Presentes: ${presentCount}\n- Ausentes: ${missingCount}`

  const filesTitle = isEn ? '## File Validation' : '## Validação de Arquivos'
  const fileLines: string[] = []
  for (const issue of issues) {
    const icon = issue.status === 'present' ? '✅' : '❌'
    const statusLabel = issue.status === 'present'
      ? (isEn ? 'present' : 'presente')
      : (isEn ? 'missing' : 'ausente')
    fileLines.push(`- ${icon} \`${issue.file}\` — ${statusLabel}`)
    if (issue.remediation) {
      fileLines.push(`  - **${isEn ? 'Fix' : 'Correção'}:** ${issue.remediation}`)
    }
  }

  const nextTitle = isEn ? '## Next Steps' : '## Próximos Passos'
  const nextBody = isEn
    ? [
        '1. Fix any missing files listed above.',
        '2. Review `.buildpact/agent-mode.yaml` and confirm capabilities.',
        '3. When ready, change `mode: prompt` → `mode: agent` in `.buildpact/agent-mode.yaml`.',
        '4. Run `npx buildpact doctor` to verify the full setup.',
        '',
        '> **Note:** Your project continues working in Prompt Mode until Agent Mode is explicitly activated.',
      ].join('\n')
    : [
        '1. Corrija os arquivos ausentes listados acima.',
        '2. Revise `.buildpact/agent-mode.yaml` e confirme as capacidades.',
        '3. Quando pronto, altere `mode: prompt` → `mode: agent` em `.buildpact/agent-mode.yaml`.',
        '4. Execute `npx buildpact doctor` para verificar a configuração completa.',
        '',
        '> **Nota:** Seu projeto continua funcionando no Modo Prompt até que o Modo Agente seja explicitamente ativado.',
      ].join('\n')

  return [title, subtitle, '', overviewTitle, overviewBody, '', filesTitle, ...fileLines, '', nextTitle, nextBody, ''].join('\n')
}

