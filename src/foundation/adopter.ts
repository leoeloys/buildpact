/**
 * Execute adoption: generate and write all BuildPact artifacts.
 * Respects mergeExisting flag — if true, skips files that already exist.
 */
export async function adopt(options: AdoptOptions): Promise<Result<AdoptResult>> {
  const {
    projectDir,
    language,
    scan,
    mergeExisting,
    ides,
    experienceLevel,
    installSquad,
    domain,
  } = options

  const projectName = scan.projectName
  const activeSquad = installSquad ? 'software' : 'none'
  const buildpactDir = join(projectDir, '.buildpact')
  const auditDir = join(buildpactDir, 'audit')
  const logger = new AuditLogger(join(auditDir, 'adopt.jsonl'))
  const templatesDir = resolveTemplatesDir()

  const created: string[] = []
  const modified: string[] = []
  const skipped: string[] = []

  try {
    await logger.log({ action: 'adopt.start', agent: 'adopter', files: [], outcome: 'success' })
    await mkdir(auditDir, { recursive: true })

    // 1. Constitution
    const constitutionPath = join(buildpactDir, 'constitution.md')
    if (mergeExisting && await fileExists(constitutionPath)) {
      skipped.push('.buildpact/constitution.md')
    } else {
      const content = generateConstitutionFromScan(scan, projectName)
      await writeFile(constitutionPath, content, 'utf-8')
      created.push('.buildpact/constitution.md')
    }

    // 2. Config
    const configPath = join(buildpactDir, 'config.yaml')
    if (mergeExisting && await fileExists(configPath)) {
      // Add schema fields if missing
      let existing = await readFile(configPath, 'utf-8')
      if (!existing.includes('buildpact_schema:')) {
        const schemaBlock = [
          `buildpact_schema: ${CURRENT_SCHEMA_VERSION}`,
          `created_by_cli: "${CLI_VERSION}"`,
          `last_upgraded_by_cli: "${CLI_VERSION}"`,
          '',
        ].join('\n')
        const lines = existing.split('\n')
        const insertIdx = lines.findIndex(l => !l.startsWith('#') && l.trim().length > 0)
        if (insertIdx >= 0) {
          lines.splice(insertIdx, 0, schemaBlock)
        } else {
          lines.unshift(schemaBlock)
        }
        existing = lines.join('\n')
        await writeFile(configPath, existing, 'utf-8')
        modified.push('.buildpact/config.yaml')
      } else {
        skipped.push('.buildpact/config.yaml')
      }
    } else {
      const content = generateConfigYaml(projectName, language, experienceLevel, activeSquad)
      await writeFile(configPath, content, 'utf-8')
      created.push('.buildpact/config.yaml')
    }

    // 3. Project context
    const contextPath = join(buildpactDir, 'project-context.md')
    if (mergeExisting && await fileExists(contextPath)) {
      skipped.push('.buildpact/project-context.md')
    } else {
      const content = generateContextFromScan(scan, projectName, language, experienceLevel, activeSquad)
      await writeFile(contextPath, content, 'utf-8')
      created.push('.buildpact/project-context.md')
    }

    // 4. Profiles
    const profilesDest = join(buildpactDir, 'profiles')
    if (mergeExisting && await fileExists(profilesDest)) {
      skipped.push('.buildpact/profiles')
    } else {
      await copyDir(join(templatesDir, 'profiles'), profilesDest)
      created.push('.buildpact/profiles')
    }

    // 5. IDE configs — always install BuildPact slash commands even if IDE is already configured,
    //    because having .claude/commands doesn't mean .claude/commands/bp/ exists.
    for (const ide of ides) {
      await installIdeConfig(ide, projectDir, templatesDir)
      created.push(`IDE: ${ide}`)
    }

    // 6. Squad
    if (installSquad) {
      const squadDest = join(buildpactDir, 'squads', 'software')
      if (mergeExisting && await fileExists(squadDest)) {
        skipped.push('.buildpact/squads/software')
      } else {
        await copyDir(join(templatesDir, 'squads', 'software'), squadDest)
        created.push('.buildpact/squads/software')
      }
    }

    // 7. DECISIONS.md and STATUS.md (skip if exist)
    for (const file of ['DECISIONS.md', 'STATUS.md']) {
      const filePath = join(projectDir, file)
      if (await fileExists(filePath)) {
        skipped.push(file)
      } else {
        const vars: Record<string, string> = {
          project_name: projectName,
          language,
          experience_level: experienceLevel,
          active_squad: activeSquad,
          created_at: new Date().toISOString().slice(0, 10),
        }
        const template = await readFile(join(templatesDir, file), 'utf-8')
        await writeFile(filePath, interpolate(template, vars), 'utf-8')
        created.push(file)
      }
    }

    await logger.log({
      action: 'adopt.complete',
      agent: 'adopter',
      files: [...created, ...modified],
      outcome: 'success',
    })

    return ok({ created, modified, skipped })
  } catch (cause) {
    try {
      await logger.log({ action: 'adopt.error', agent: 'adopter', files: [], outcome: 'failure', error: String(cause) })
    } catch { /* swallow */ }
    return err({
      code: 'ADOPT_FAILED',
      i18nKey: 'error.adopt.failed',
      cause,
    })
  }
}

// ---------------------------------------------------------------------------
// IDE config (simplified from installer.ts)
// ---------------------------------------------------------------------------

async function installIdeConfig(ide: IdeId, projectDir: string, tplDir: string): Promise<void> {
  switch (ide) {
    case 'claude-code': {
      const commandsDir = join(projectDir, '.claude', 'commands', 'bp')
      await mkdir(commandsDir, { recursive: true })
      await copyDir(join(tplDir, 'commands'), commandsDir)
      const claudeMd = join(projectDir, 'CLAUDE.md')
      if (!await fileExists(claudeMd)) {
        await writeFile(
          claudeMd,
          '# CLAUDE.md — BuildPact Project\n\nSee .buildpact/constitution.md for project rules.\n\nBuildPact slash commands are available as /bp:specify, /bp:plan, /bp:execute, /bp:verify, /bp:quick, /bp:constitution, /bp:squad, /bp:optimize, /bp:doctor.\n',
          'utf-8',
        )
      }
      break
    }
    case 'cursor': {
      await mkdir(join(projectDir, '.cursor', 'rules'), { recursive: true })
      const cursorRules = join(projectDir, '.cursorrules')
      if (!await fileExists(cursorRules)) {
        await writeFile(cursorRules, '# BuildPact — Cursor Rules\n\nSee .buildpact/constitution.md for project rules.\n', 'utf-8')
      }
      break
    }
    case 'gemini': {
      await mkdir(join(projectDir, '.gemini'), { recursive: true })
      break
    }
    case 'codex': {
      await mkdir(join(projectDir, '.codex'), { recursive: true })
      break
    }
  }
}
