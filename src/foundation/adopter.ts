/** Generate a pre-filled constitution.md from scan results. */
export function generateConstitutionFromScan(scan: ScanResult, projectName: string): string {
  const createdAt = new Date().toISOString().slice(0, 10)
  const sections: string[] = []

  sections.push(`# Project Constitution — ${projectName}`)
  sections.push('')
  sections.push(`> Auto-generated by \`buildpact adopt\` on ${createdAt}.`)
  sections.push('> Review and adjust these rules — they were inferred from your existing project.')
  sections.push('')
  sections.push('---')
  sections.push('')

  // Coding Standards
  sections.push('## Immutable Principles')
  sections.push('')
  sections.push('### Coding Standards')

  if (scan.languages.length > 0) {
    sections.push(`- Primary language(s): ${scan.languages.join(', ')}`)
  }

  for (const linter of scan.linters) {
    sections.push(`- ${linter.tool} enforced (config: ${linter.configFile})`)
    for (const rule of linter.extractedRules) {
      sections.push(`  - ${rule}`)
    }
  }

  if (scan.linters.length === 0) {
    sections.push('- No linter detected — consider adding one')
  }

  sections.push('- Spec before code — no implementation begins without a reviewed specification')
  sections.push('- Atomic commits — one commit per completed task')
  sections.push('')

  // Compliance
  sections.push('### Compliance Requirements')
  sections.push('- None detected — add project-specific compliance requirements here')
  sections.push('')

  // Architectural Constraints
  sections.push('### Architectural Constraints')

  for (const pkg of scan.packageManagers) {
    sections.push(`- Package manager: ${pkg.name} (${pkg.configFile})`)
    if (pkg.version) sections.push(`  - Project version: ${pkg.version}`)
  }

  sections.push('- No circular dependencies between modules')
  sections.push('')

  // Quality Gates
  sections.push('### Quality Gates')

  for (const ci of scan.ci) {
    const gates = ci.qualityGates.length > 0 ? ` (gates: ${ci.qualityGates.join(', ')})` : ''
    sections.push(`- CI: ${ci.platform}${gates}`)
  }

  sections.push('- All tests must pass before any commit')
  sections.push('')

  // Domain
  sections.push('## Domain-Specific Rules')
  sections.push('- None by default — add Squad-specific rules here')
  sections.push('')

  // Version History
  sections.push('---')
  sections.push('')
  sections.push('## Version History')
  sections.push('| Date | Change | Reason |')
  sections.push('|------|--------|--------|')
  sections.push(`| ${createdAt} | Initial creation | Project adopted via \`buildpact adopt\` |`)
  sections.push('')

  return sections.join('\n')
}

/** Generate a pre-filled project-context.md from scan results. */
export function generateContextFromScan(
  scan: ScanResult,
  projectName: string,
  language: SupportedLanguage,
  experienceLevel: string,
  activeSquad: string,
): string {
  const createdAt = new Date().toISOString().slice(0, 10)
  const sections: string[] = []

  // Frontmatter
  sections.push('---')
  sections.push(`project_name: "${projectName}"`)
  sections.push(`created_at: "${createdAt}"`)
  sections.push(`language: "${language}"`)
  sections.push(`document_language: "en"`)
  sections.push(`experience_level: "${experienceLevel}"`)
  sections.push(`active_squad: "${activeSquad}"`)
  sections.push(`active_model_profile: "balanced"`)
  sections.push(`workflow_phase: "adopted"`)
  sections.push('---')
  sections.push('')

  sections.push(`# ${projectName} — Project Context`)
  sections.push('')
  sections.push('> Auto-generated by `buildpact adopt`. Update this file with project-specific details.')
  sections.push('')
  sections.push('---')
  sections.push('')

  // Tech stack
  sections.push('## Technology Stack')
  sections.push('')

  if (scan.languages.length > 0) {
    sections.push(`- **Languages:** ${scan.languages.join(', ')}`)
  }
  for (const pkg of scan.packageManagers) {
    sections.push(`- **Package manager:** ${pkg.name} (${pkg.configFile})`)
  }
  for (const linter of scan.linters) {
    sections.push(`- **Linter/formatter:** ${linter.tool} (${linter.configFile})`)
  }
  for (const ci of scan.ci) {
    sections.push(`- **CI/CD:** ${ci.platform} (${ci.configFile})`)
  }
  if (scan.git) {
    sections.push(`- **Git:** ${scan.git.commitCount} commits, ${scan.git.contributorCount} contributor(s)`)
  }

  sections.push('')
  sections.push('---')
  sections.push('')
  sections.push('## What is this project?')
  sections.push('')
  sections.push('<!-- Describe your project here -->')
  sections.push('')
  sections.push('---')
  sections.push('')
  sections.push('## Current State')
  sections.push('')
  sections.push('- Next action: Run `buildpact specify` to begin specification pipeline')
  sections.push('')

  return sections.join('\n')
}

// ---------------------------------------------------------------------------
// Config generation
// ---------------------------------------------------------------------------

function generateConfigYaml(
  projectName: string,
  language: SupportedLanguage,
  experienceLevel: string,
  activeSquad: string,
): string {
  const createdAt = new Date().toISOString().slice(0, 10)
  return [
    '# BuildPact project configuration',
    '# Generated by: buildpact adopt',
    '',
    `buildpact_schema: ${CURRENT_SCHEMA_VERSION}`,
    `created_by_cli: "${CLI_VERSION}"`,
    `last_upgraded_by_cli: "${CLI_VERSION}"`,
    '',
    `project_name: "${projectName}"`,
    `language: "${language}"`,
    `experience_level: "${experienceLevel}"`,
    `active_squad: "${activeSquad}"`,
    `active_model_profile: "balanced"`,
    `created_at: "${createdAt}"`,
    '',
  ].join('\n')
}

