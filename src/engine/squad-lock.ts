/** Recursively collect all files in a directory, returning relative paths */
async function collectFiles(dir: string, baseDir: string): Promise<string[]> {
  const collected: string[] = []
  let entries: string[]
  try {
    entries = await readdir(dir)
  } catch {
    return collected
  }

  for (const entry of entries) {
    const fullPath = join(dir, entry)
    const st = await stat(fullPath)
    if (st.isDirectory()) {
      const nested = await collectFiles(fullPath, baseDir)
      collected.push(...nested)
    } else {
      collected.push(relative(baseDir, fullPath))
    }
  }

  return collected.sort()
}

/**
 * Build a LockedSquad entry by scanning a squad directory.
 * Reads all files, computes per-file hashes, and an overall content hash.
 */
export async function buildLockedEntry(
  squadDir: string,
  squadName: string,
  nowIso?: string,
): Promise<Result<LockedSquad>> {
  const files = await collectFiles(squadDir, squadDir)
  const fileHashes: Record<string, string> = {}
  const allContent: string[] = []

  // Read squad.yaml for version
  let version = 'unknown'
  try {
    const yamlContent = await readFile(join(squadDir, 'squad.yaml'), 'utf-8')
    for (const line of yamlContent.split('\n')) {
      const trimmed = line.trim()
      if (trimmed.startsWith('version:')) {
        version = trimmed.slice('version:'.length).trim().replace(/^["']|["']$/g, '')
        break
      }
    }
  } catch {
    // squad.yaml missing — proceed with 'unknown'
  }

  for (const filePath of files) {
    try {
      const content = await readFile(join(squadDir, filePath), 'utf-8')
      const hash = sha256(content)
      fileHashes[filePath] = hash
      allContent.push(`${filePath}:${hash}`)
    } catch {
      // Skip unreadable files
    }
  }

  const contentHash = sha256(allContent.join('\n'))

  return ok({
    name: squadName,
    version,
    contentHash,
    lockedAt: nowIso ?? new Date().toISOString(),
    files: fileHashes,
  })
}

/**
 * Read the lockfile from disk. Returns empty lockfile if not found.
 */
export async function readLockfile(projectDir: string): Promise<Result<SquadLockfile>> {
  const lockPath = join(projectDir, LOCKFILE_PATH)
  try {
    const content = await readFile(lockPath, 'utf-8')
    return parseLockfile(content)
  } catch {
    return ok({ lockVersion: 1, squads: {} })
  }
}

/**
 * Write the lockfile to disk.
 */
export async function writeLockfile(projectDir: string, lock: SquadLockfile): Promise<Result<void>> {
  const lockPath = join(projectDir, LOCKFILE_PATH)
  try {
    await writeFile(lockPath, serializeLockfile(lock), 'utf-8')
    return ok(undefined)
  } catch (cause) {
    return err({
      code: ERROR_CODES.FILE_WRITE_FAILED,
      i18nKey: 'error.file.write_failed',
      params: { path: lockPath, reason: String(cause) },
      cause,
    })
  }
}

/**
 * Check a single squad for drift against the lockfile entry.
 */
export async function checkSquadDrift(
  squadDir: string,
  locked: LockedSquad,
): Promise<DriftResult> {
  const currentFiles = await collectFiles(squadDir, squadDir)
  const lockedFilePaths = Object.keys(locked.files)

  const changedFiles: string[] = []
  const addedFiles: string[] = []
  const removedFiles: string[] = []

  // Check for changed and added files
  for (const filePath of currentFiles) {
    const lockedHash = locked.files[filePath]
    if (lockedHash === undefined) {
      addedFiles.push(filePath)
      continue
    }
    try {
      const content = await readFile(join(squadDir, filePath), 'utf-8')
      const currentHash = sha256(content)
      if (currentHash !== lockedHash) {
        changedFiles.push(filePath)
      }
    } catch {
      // Can't read — treat as removed
      removedFiles.push(filePath)
    }
  }

  // Check for removed files
  for (const filePath of lockedFilePaths) {
    if (!currentFiles.includes(filePath)) {
      removedFiles.push(filePath)
    }
  }

  return {
    name: locked.name,
    drifted: changedFiles.length > 0 || addedFiles.length > 0 || removedFiles.length > 0,
    changedFiles,
    addedFiles,
    removedFiles,
  }
}

/**
 * Check all squads for drift. Returns drift results per squad.
 */
export async function checkAllSquadDrift(projectDir: string): Promise<Result<DriftResult[]>> {
  const lockResult = await readLockfile(projectDir)
  if (!lockResult.ok) return lockResult as Result<DriftResult[]>

  const lock = lockResult.value
  const results: DriftResult[] = []

  for (const [name, locked] of Object.entries(lock.squads)) {
    const squadDir = join(projectDir, '.buildpact', 'squads', name)
    const drift = await checkSquadDrift(squadDir, locked)
    results.push(drift)
  }

  return ok(results)
}

/**
 * Lock a single squad (add or update its entry in the lockfile).
 */
export async function lockSquad(projectDir: string, squadName: string): Promise<Result<void>> {
  const squadDir = join(projectDir, '.buildpact', 'squads', squadName)
  const lockResult = await readLockfile(projectDir)
  if (!lockResult.ok) return lockResult as Result<void>

  const lock = lockResult.value
  const entryResult = await buildLockedEntry(squadDir, squadName)
  if (!entryResult.ok) return entryResult as Result<void>

  lock.squads[squadName] = entryResult.value
  return writeLockfile(projectDir, lock)
}

/**
 * Update all squad locks (re-scan and re-hash all installed squads).
 */
export async function updateAllLocks(projectDir: string): Promise<Result<string[]>> {
  const squadsDir = join(projectDir, '.buildpact', 'squads')
  let entries: string[]
  try {
    entries = await readdir(squadsDir)
  } catch {
    return ok([])
  }

  const lock: SquadLockfile = { lockVersion: 1, squads: {} }
  const updated: string[] = []

  for (const entry of entries) {
    const squadDir = join(squadsDir, entry)
    const st = await stat(squadDir)
    if (!st.isDirectory()) continue

    const entryResult = await buildLockedEntry(squadDir, entry)
    if (entryResult.ok) {
      lock.squads[entry] = entryResult.value
      updated.push(entry)
    }
  }

  const writeResult = await writeLockfile(projectDir, lock)
  if (!writeResult.ok) return writeResult as Result<string[]>

  return ok(updated)
}
