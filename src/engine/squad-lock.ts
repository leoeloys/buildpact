/**
 * Squad Lock — version pinning with content hash integrity.
 * Creates and manages `.buildpact/squad-lock.yaml` to track installed
 * squad versions and detect file drift.
 * @see Epic 16.1: Squad Version Pinning & Lockfile
 */

import { readFile, writeFile, readdir, stat } from 'node:fs/promises'
import { join, relative } from 'node:path'
import { createHash } from 'node:crypto'
import { ok, err, ERROR_CODES } from '../contracts/errors.js'
import type { Result } from '../contracts/errors.js'

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** A single locked squad entry */
export interface LockedSquad {
  /** Squad name */
  name: string
  /** Semantic version from squad.yaml */
  version: string
  /** SHA-256 hash of all squad files concatenated (sorted by path) */
  contentHash: string
  /** ISO 8601 timestamp when locked */
  lockedAt: string
  /** Individual file hashes for granular drift detection */
  files: Record<string, string>
}

/** The full lockfile structure */
export interface SquadLockfile {
  /** Lockfile schema version */
  lockVersion: 1
  /** Map of squad name -> locked entry */
  squads: Record<string, LockedSquad>
}

/** Result of a drift check for a single squad */
export interface DriftResult {
  /** Squad name */
  name: string
  /** Whether any drift was detected */
  drifted: boolean
  /** Files that changed */
  changedFiles: string[]
  /** Files that were added (not in lock) */
  addedFiles: string[]
  /** Files that were removed (in lock but missing on disk) */
  removedFiles: string[]
}

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/** Relative path to the lockfile within project root */
export const LOCKFILE_PATH = '.buildpact/squad-lock.yaml'

// ---------------------------------------------------------------------------
// Pure functions
// ---------------------------------------------------------------------------

/** Compute SHA-256 hash of a string */
export function sha256(content: string): string {
  return createHash('sha256').update(content, 'utf-8').digest('hex')
}

/** Serialize a lockfile to YAML-like format */
export function serializeLockfile(lock: SquadLockfile): string {
  const lines: string[] = [
    '# BuildPact Squad Lockfile — DO NOT EDIT MANUALLY',
    '# Generated by: buildpact squad add / buildpact squad update',
    `lock_version: ${lock.lockVersion}`,
    'squads:',
  ]

  for (const [name, entry] of Object.entries(lock.squads)) {
    lines.push(`  ${name}:`)
    lines.push(`    version: "${entry.version}"`)
    lines.push(`    content_hash: "${entry.contentHash}"`)
    lines.push(`    locked_at: "${entry.lockedAt}"`)
    lines.push(`    files:`)
    for (const [filePath, hash] of Object.entries(entry.files)) {
      lines.push(`      "${filePath}": "${hash}"`)
    }
  }

  return lines.join('\n') + '\n'
}

/** Parse a lockfile from YAML-like text */
export function parseLockfile(content: string): Result<SquadLockfile> {
  const lockfile: SquadLockfile = { lockVersion: 1, squads: {} }

  const lines = content.split('\n')
  let currentSquad: string | undefined
  let inFiles = false

  for (const raw of lines) {
    const line = raw.trimEnd()
    if (!line || line.startsWith('#')) continue

    const trimmed = line.trimStart()
    const indent = line.length - trimmed.length

    if (trimmed.startsWith('lock_version:')) {
      const val = trimmed.slice('lock_version:'.length).trim()
      if (val !== '1') {
        return err({
          code: ERROR_CODES.CONFIG_INVALID,
          i18nKey: 'error.squad.lock_version_unsupported',
          params: { version: val },
        })
      }
      continue
    }

    if (trimmed === 'squads:') continue

    // Squad name line (indent 2, ends with colon, no value)
    if (indent === 2 && trimmed.endsWith(':') && !trimmed.includes('"')) {
      currentSquad = trimmed.slice(0, -1)
      lockfile.squads[currentSquad] = {
        name: currentSquad,
        version: '',
        contentHash: '',
        lockedAt: '',
        files: {},
      }
      inFiles = false
      continue
    }

    if (!currentSquad) continue
    const entry = lockfile.squads[currentSquad]!

    if (indent === 4 && trimmed.startsWith('version:')) {
      entry.version = trimmed.slice('version:'.length).trim().replace(/^"|"$/g, '')
      inFiles = false
      continue
    }
    if (indent === 4 && trimmed.startsWith('content_hash:')) {
      entry.contentHash = trimmed.slice('content_hash:'.length).trim().replace(/^"|"$/g, '')
      inFiles = false
      continue
    }
    if (indent === 4 && trimmed.startsWith('locked_at:')) {
      entry.lockedAt = trimmed.slice('locked_at:'.length).trim().replace(/^"|"$/g, '')
      inFiles = false
      continue
    }
    if (indent === 4 && trimmed === 'files:') {
      inFiles = true
      continue
    }

    // File hash entry (indent 6)
    if (indent === 6 && inFiles && currentSquad) {
      const colonIdx = trimmed.indexOf(':')
      if (colonIdx !== -1) {
        const filePath = trimmed.slice(0, colonIdx).trim().replace(/^"|"$/g, '')
        const hash = trimmed.slice(colonIdx + 1).trim().replace(/^"|"$/g, '')
        entry.files[filePath] = hash
      }
    }
  }

  return ok(lockfile)
}

